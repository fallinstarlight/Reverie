<?php
/* Product model, represents a product in the system, used to create product objects from database records */

class product{
    /* Product properties */
    public string $code;
    public string $name;
    public string $price;
    public string $description;
    public int $amount;
    public string $type;
    public string $photo;
    public string $state;

    /* Constructor to create a product object from an associative array */
    public function __construct(array $data)
    {
        $fields = ['Code', 'Product', 'Price', 'Description', 'Amount', 'Type'];
        foreach($fields as $field){
            if(!isset($data[$field])){
                throw new InvalidArgumentException("Missing info: {$field}");
            }
        }
        
        if(!is_string($data['Code'])){
            throw new InvalidArgumentException("Incorrect ID: {$data['ID']}, must be numeric");
        }
        if($data['Amount'] < 0){
            throw new InvalidArgumentException("Cannot have negative stock");
        }

        /* If all validations pass, set the product properties */
        $this -> code = $data['Code'];
        $this -> name = $data['Product'];
        $this -> price = $data['Price'];
        $this -> description = $data['Description'];
        $this -> amount = $data['Amount'];
        $this -> type = $data['Type']; 
        $this -> state = (isset($data['State'])) ? $data['State'] : ' ';
        $this -> photo = (isset($data['Photo'])) ? $data['Photo'] : ' ';
    }
}


